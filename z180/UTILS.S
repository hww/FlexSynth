	SECTION	ICODE

;**********************************************
; ЧТЕНИЕ ЗАПИСЬ ПАМЯТИ z80
;**********************************************
MEM_RD:
	LD	A,(HL)
	RET

MEM_WR:
	LD	(HL),A
	RET
;**********************************************
; ЧТЕНИЕ ЗАПИСЬ В ПОРТ
;**********************************************
INP:
	IN	A,(C)
	RET

OUTP:
	OUT	(C),A
	RET
TST:
	LD	(0FFF1H),A
	LD	(0FFF2H),HL
	LD	(0FFF4H),DE
	LD	(0FFF6H),BC
	RET
	